import javax.swing.*;

public class MyFirstFrame {
    
    public static void main(String[] args) {
        JFrame myWindow = new JFrame();
        myWindow.setSize(300, 200); // Window size
        myWindow.setTitle("My First Frame"); // Window title
        myWindow.setLocation(100, 50); // Set position 100px from screen left and 50px from screen top
        myWindow.setVisible(true); // Set visible
    }
}
